# Python Escape Sequences Demo
# ==============================

# 🔹 New Line
print("Hello\nWorld")

# 🔹 Tab (horizontal space)
print("Hello\tWorld")

# 🔹 Backslash
print("This is a backslash: \\")

# 🔹 Single Quote
print('It\'s a good day')

# 🔹 Double Quote
print("He said \"Hello\"")

# 🔹 Carriage Return (overwrites from start)
print("Hello\rWorld")   # Output: World

# 🔹 Backspace (removes previous character)
print("Helloo\b")   # Output: Hello

# 🔹 Form Feed (less used, page break)
print("Hello\fWorld")

# 🔹 Vertical Tab
print("Hello\vWorld")

# 🔹 Unicode Character
print("\u2764")   # ❤

# 🔹 ASCII Character using octal
print("\101")   # A

# 🔹 ASCII Character using hex
print("\x41")   # A

